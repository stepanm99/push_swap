#include "../incl/push_swap.h"

void	rotate_a(t_dt *dt)
{
	dt->a_length = dt->a_length;
	write(1, "ra\n", 3);
}

void	rotate_b(t_dt *dt)
{
	dt->a_length = dt->a_length;
	write(1, "rb\n", 3);
}

void	rotate_ab(t_dt *dt)
{
	dt->a_length = dt->a_length;
	write(1, "rr\n", 3);
}
